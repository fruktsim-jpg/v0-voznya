/**
 * ЕДИНЫЙ источник истины по лимитным подаркам (Release 2.2, P0).
 *
 * Лимитный = подарок, чей telegram_gift_id входит в этот канонический набор.
 * Список прислан как источник истины; ВСЁ остальное — обычные подарки. Раньше
 * «лимитность» вычислялась по-разному (stock != null в магазине, hardcoded true
 * в инвентаре) — это расходилось с реальностью. Теперь одна функция на весь сайт.
 *
 * Экономика лимитки: база 50★, цена = обычная × 1.5 (см. economy-rules /
 * миграцию 0030). price_multiplier для лимитки = 1.5.
 */

/** Канонические telegram_gift_id лимитных (сезонных collectible) подарков. */
export const LIMITED_GIFT_IDS: ReadonlySet<string> = new Set([
  '5956217000635139069',
  '5922558454332916696',
  '5800655655995968830',
  '5801108895304779062',
  '5866352046986232958',
  '5893356958802511476',
  '5935895822435615975',
  '5969796561943660080',
])

/** Множитель цены лимитного подарка к обычной формуле тира. */
export const LIMITED_PRICE_MULTIPLIER = 1.5

/**
 * Лимитный ли подарок — по его telegram_gift_id. Единственная точка правды для
 * бейджей/редкости/фильтров. ``null``/неизвестный id → обычный.
 */
export function isLimitedGiftId(telegramGiftId: string | null | undefined): boolean {
  if (!telegramGiftId) return false
  return LIMITED_GIFT_IDS.has(telegramGiftId)
}
